async function populateBlogPosts() {
  try {
    console.log('📝 Starting to populate blog posts...');

    for (let i = 0; i < blogPosts.length; i++) {
      const postData = blogPosts[i];
      const slug = generateSlug(postData.title);

      // Check if post already exists
      const existing = await prisma.blogPost.findUnique({
        where: { slug },
      });

      if (existing) {
        console.log(`⏭️  Post "${postData.title}" already exists, skipping...`);
        continue;
      }

      const post = await prisma.blogPost.create({
        data: {
          title: postData.title,
          slug: slug,
          excerpt: postData.excerpt,
          content: postData.content,
          metaTitle: postData.title,
          metaDescription: postData.excerpt,
          keywords: postData.keywords,
          isPublished: true,
          publishedAt: new Date(),
        },
      });

      console.log(`✅ Created post: "${post.title}" (ID: ${post.id})`);
    }

    console.log(`\n✅ Successfully populated ${blogPosts.length} blog posts!`);
  } catch (error) {
    console.error('❌ Error populating blog posts:', error);
    throw error;
  } finally {
    await prisma.$disconnect();
  }
}

// Run the script
populateBlogPosts()
  .then(() => {
    console.log('\n✅ Script completed successfully');
    process.exit(0);
  })
  .catch((error) => {
    console.error('\n❌ Script failed:', error);
    process.exit(1);
  });
